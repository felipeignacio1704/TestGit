def suma( a,  b):
    resultado = a + b
    return resultado

def resta( a,  b):
    resultado = a - b
    return resultado

def multiplicar( a,  b):
    resultado = a * b
    return resultado

def dividir(a, b):
    if b == 0:
        return "Error: No se puede dividir por cero."
    else:
        resultado = a / b
        return resultado
    

